package Data;

public class boundingBox {
	private spriteInfo spriteData;
	private int xCoord;
	private int yCoord;
    private int height;
    private int width;
    
    public boundingBox(spriteInfo spriteData, int xCoord, int yCoord, int height, int width) {
    	this.spriteData = spriteData;
    	this.xCoord = xCoord;
    	this.yCoord = yCoord;
    	this.height = height;
    	this.width = width;
    }
    
    

}
